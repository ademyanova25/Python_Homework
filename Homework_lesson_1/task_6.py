a = int(input('Введите км первого дня: '))
b = int(input('Введите км желаемого результата: '))
c = a * 0.1 + a
d = 1
while c <= b:
    print(f'{d}-й день: {c:.3}.')
    c = c * 0.1 + c
    d = d + 1
print(f'{d}-й день: {c:.3}.')
print(f'На {d}-й день спортсмен достиг результата — не менее {b} км.')
